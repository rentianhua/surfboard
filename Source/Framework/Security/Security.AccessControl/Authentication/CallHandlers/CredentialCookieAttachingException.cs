    [Serializable]
    public class CredentialCookieAttachingException : Exception
    {
        public CredentialCookieAttachingException()
        {
        }

        public CredentialCookieAttachingException(string message)
                : base(message)
        {
        }

        public CredentialCookieAttachingException(string message, Exception inner)
                : base(message, inner)
        {
        }

        protected CredentialCookieAttachingException(SerializationInfo info, StreamingContext context)
                : base(info, context)
        {
        }
    }

